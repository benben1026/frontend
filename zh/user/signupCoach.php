3. 为了保证学员用户能够得到可靠的指导，教练用户需要在学员用户提出问题后12小时内解答学员的疑问。promeXeus将会发邮件提醒双方用户。

评价协议：
1. 学员用户在完成一个阶段的训练内容后，需要评价该教练用户。等级从1星到5星。目的：一是，保证学员用户获得优质的指导；二是，根据学员用户的评价，教练用户可以得到反馈，提高自己的综合能力，为更多的学员用户提供更好的服务。


宣传协议：
1. 教练用户需要在注册的时候填写真实的个人资料，方便学员用户很快地搜索到教练。丰富而真实的资料也方便用户更好地了解教练。
2. 为了更好地推广教练用户，教练用户需要丰富个人资料，向学员用户更好地介绍自己。
3. 教练用户应该根据每个学员的情况，对该学员的训练内容进行对立的设定和优化。

支付协议：
1. 为了保证学员用户和教练用户的资金安全，promeXeus使⽤用支付宝（中国大陆），贝宝Paypal（海外）转账的方式。学员用户需要先将金额转入promeXeus的指定账户，在promeXeus核实之后，学员用户的账户会有与转入金额相等的训练币，随着学员用户训练的进行，训练费用会逐渐从学员用户的训练币账户中扣除，与此同时，promeXeus将会把实际的费用转入教练用户的账户之中。
